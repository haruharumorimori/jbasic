/*
 * 論理型の変数に「true」もしくは「false」を代入し、表示させるプログラムを作成してください。
 */
public class No001 {
	public static void main(String[] args) {
		boolean a ;
		a = true;
		boolean b;
		b = false;

		System.out.println(a);
		System.out.println(b);
	}
}

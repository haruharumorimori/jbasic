package Chapter2;

public class Sumple1_6 {
	public static void main(String[] args) {
	int num;
	System.out.println("後置の場合");
	num=10;
	System.out.println(num++);
	System.out.println(num);

	System.out.println("前置の場合");
	num=10;
	System.out.println(++num);
	System.out.println(num);
	}
}
